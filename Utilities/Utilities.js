// This compare function should have the logic
function comparefn_ascending (x, y) {
    if (x > y) {
        return 1;
    } else if (x < y) {
        return -1;
    } else return 0;
}

function comparefn_descending (x, y) {
    if (x > y) {
        return -1;
    } else if (x < y) {
        return 1;
    } else return 0;
}


